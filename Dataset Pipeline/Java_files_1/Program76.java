public class Program76_CompareTwoNumbers {
    public static void main(String[] args) {
        int a = 15, b = 20;

        if (a > b) {
            System.out.println(1);
        } else if (a < b) {
            System.out.println(2);
        } else {
            System.out.println(0);
        }
    }
}