public class TestScoped {

    public static void main(String[] args_main) {
        for (int i_main = 1; i_main <= 3; i_main++) {
            for (int j_main = 1; j_main <= 3; j_main++) {
                System.out.println(i_main + " x " + j_main + " = " + (i_main * j_main));
            }
        }
    }
}
